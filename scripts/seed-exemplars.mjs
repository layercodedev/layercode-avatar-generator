// scripts/seed-exemplars.mjs
// Compresses /public/claymation-refs/*.png into base64 WebP strings
// and writes lib/default-exemplars.ts for first-load seeding.
// Run with: node scripts/seed-exemplars.mjs

import sharp from "sharp";
import { readdir, readFile, writeFile } from "node:fs/promises";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const REFS_DIR = join(__dirname, "..", "public", "claymation-refs");
const OUT_FILE = join(__dirname, "..", "lib", "default-exemplars.ts");

const TARGET_SIZE = 600;
const QUALITY = 80;

const files = (await readdir(REFS_DIR))
  .filter((f) => /^claymation-\d+\.png$/.test(f))
  .sort();

if (files.length === 0) {
  console.error("No claymation-N.png files found in", REFS_DIR);
  process.exit(1);
}

const entries = [];
for (const file of files) {
  const buf = await readFile(join(REFS_DIR, file));
  const out = await sharp(buf)
    .resize(TARGET_SIZE, TARGET_SIZE, { fit: "cover" })
    .webp({ quality: QUALITY })
    .toBuffer();
  const base64 = out.toString("base64");
  const name = `Claymation reference ${file.match(/\d+/)[0]}`;
  entries.push({ name, mimeType: "image/webp", imageData: base64 });
  console.log(`${file}: ${(buf.length / 1024).toFixed(0)}KB → ${(out.length / 1024).toFixed(0)}KB`);
}

const ts = `// lib/default-exemplars.ts
// Auto-generated by scripts/seed-exemplars.mjs. Do not edit by hand.
// Regenerate after replacing files in public/claymation-refs/.

export interface DefaultExemplar {
  name: string;
  mimeType: string;
  imageData: string;
}

export const DEFAULT_EXEMPLARS: DefaultExemplar[] = ${JSON.stringify(entries, null, 2)};
`;

await writeFile(OUT_FILE, ts);
console.log(`\nWrote ${OUT_FILE} (${entries.length} exemplars)`);
